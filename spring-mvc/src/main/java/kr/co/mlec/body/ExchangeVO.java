package kr.co.mlec.body;

public class ExchangeVO {
	String date;
	double value;
	
	public ExchangeVO() {
		super();
	}
	
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
	
}
